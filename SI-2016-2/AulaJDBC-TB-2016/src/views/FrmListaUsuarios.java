    private void jtListaUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtListaUsuariosMouseClicked
        int index = jtListaUsuarios.getSelectedRow();
        
        DefaultTableModel modelo = (DefaultTableModel) jtListaUsuarios.getModel();
        
        Usuario aux = new Usuario();
        
        aux.setId(Integer.parseInt(modelo.getValueAt(index, 0).toString()));
        aux.setNome(modelo.getValueAt(index, 1).toString());
        aux.setSenha(modelo.getValueAt(index, 2).toString());
        aux.setDescricao(modelo.getValueAt(index, 3).toString());
        aux.setPerfil(Integer.parseInt(modelo.getValueAt(index, 4).toString()));
        
        frmUsuario.tfId.setText(String.valueOf(aux.getId()));
        frmUsuario.tfNome.setText(aux.getNome());
        frmUsuario.tfSenha.setText(aux.getSenha());
        frmUsuario.tfDescricao.setText(aux.getDescricao());
        frmUsuario.tfPerfil.setText(String.valueOf(aux.getPerfil()));
        
        frmUsuario.setVisible(true);
        
    }//GEN-LAST:event_jtListaUsuariosMouseClicked

