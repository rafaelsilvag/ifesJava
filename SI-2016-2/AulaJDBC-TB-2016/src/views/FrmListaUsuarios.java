    public void addRowsToTable(){
        DefaultTableModel modelo = (DefaultTableModel) jtListaUsuarios.getModel();
        
        modelo.getDataVector().removeAllElements();
        
        Connection conn = database.DatabaseFactory
                .getDatabase("postgresql").connect();
        
        UsuarioCRUD usuarioCrud = new UsuarioCRUD();
        
        Object rowData[] = new Object[5];
        
        if(tfFiltro.getText().equals("")){
            for(Usuario aux: usuarioCrud.read(conn)){
                rowData[0] = aux.getId();
                rowData[1] = aux.getNome();
                rowData[2] = aux.getSenha();
                rowData[3] = aux.getDescricao();
                rowData[4] = aux.getPerfil();

                modelo.addRow(rowData);
            }
        }else{
            for(Usuario aux: usuarioCrud.read(conn, tfFiltro.getText())){
                rowData[0] = aux.getId();
                rowData[1] = aux.getNome();
                rowData[2] = aux.getSenha();
                rowData[3] = aux.getDescricao();
                rowData[4] = aux.getPerfil();

                modelo.addRow(rowData);
            }
        }
        
        database.DatabaseFactory.getDatabase("postgresql").disconnect(conn);
    }
